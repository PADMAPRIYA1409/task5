// number method
var num1=38
console.log(typeof(num1))

// string
console.log(typeof(num1.toString()))

// tofixed..roundoff value
var num2=39.50000
console.log(num2.toFixed())

// num to string
var num3=12.00000
console.log(typeof(Number(num3)))

